    private void btnMostrarDadosUserActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnMostrarDadosUserActionPerformed
        String [] dados = {"Password: ", "Nome: ", "Email: ", "Morada: ", "Telefone: ", "NIF: "}; 
        int i = 0;
            try {
                File ficheiro = new File(login+".txt");
                FileReader fr = new FileReader(ficheiro);
                BufferedReader br = new BufferedReader(fr);
                System.out.println("Dados do utilizador: "+login);
                while(br.ready()){
                    System.out.println(dados[i]);                    
                    String linha = br.readLine();
                    System.out.println(linha);
                    i += 1;
                }
                br.close();
                fr.close();
            } catch (FileNotFoundException ex) {
                ex.printStackTrace();
            } catch (IOException ioe){
                ioe.printStackTrace();
            }
    }//GEN-LAST:event_btnMostrarDadosUserActionPerformed

